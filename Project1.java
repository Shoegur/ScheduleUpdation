
package swapping;

public class Swapping 
{
    public static void main(String[] args) 
    
    {
      int x,y,temp;
      x= 15;
      y = 89;
      System.out.println("Before Swapping : x,y = "+x+", "+ + y);
      temp = x;
      x=y;
      y = temp;
      System.out.println("After Swapping: x,y = "+x+", "+ + y);
      System.out.println("New code added");
    }
    
}																																																																																																																																																												
